    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

       String username = name.getText().trim();
    String email = uemail.getText().trim();
    String password = pass.getText().trim();
    
    // VALIDATION (check first before inserting)
    if (username.isEmpty() && email.isEmpty() && password.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Please fill up all fields!",
            "Registration Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    } else if (username.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Please enter Username!",
            "Registration Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    } else if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Please enter Email!",
            "Registration Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    } else if (password.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Please enter Password!",
            "Registration Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // EMAIL FORMAT VALIDATION
    if (!email.contains("@") || !email.contains(".")) {
        JOptionPane.showMessageDialog(this,
            "Wrong format Email!\nNeed '@' and '.'",
            "Email Format Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // PASSWORD LENGTH VALIDATION
    if (password.length() < 6) {
        JOptionPane.showMessageDialog(this,
            "Password needs 6 characters or more!",
            "Password Too Short",
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // INSERT TO DATABASE
    config con = new config();
    String checkEmailSql = "SELECT * FROM tbl_user WHERE email = ?";
    
    try {
        java.sql.ResultSet rs = con.getData(checkEmailSql, email);
        
        if (rs != null && rs.next()) {
            // May nahanap = ginamit na ang email
            JOptionPane.showMessageDialog(this,
                "This email is already registered!\n" +
                "Please use a different email address.",
                "Email Already Exists",
                JOptionPane.ERROR_MESSAGE);
            return; // Huwag ituloy ang registration
        }
    } catch (java.sql.SQLException e) {
        JOptionPane.showMessageDialog(this,
            "Error checking email: " + e.getMessage(),
            "Database Error",
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // INSERT TO DATABASE (kung hindi pa ginamit ang email)
    String sql = "INSERT INTO tbl_user (username, email, pass, role, status) VALUES (?, ?, ?, ?, ?)";
    con.addRecord(sql, username, email, password, "user", "Pending");
    
    // SUCCESS MESSAGE
    JOptionPane.showMessageDialog(this,
        "Registration Successful!\n\n" +
        "Your account is pending approval.\n" +
        "Please wait for admin to approve your account.",
        "Success",
        JOptionPane.INFORMATION_MESSAGE);
    
    // CLEAR FIELDS
    name.setText("");
    uemail.setText("");
    pass.setText("");
    
    // GO TO LOGIN
    this.dispose();
    loginform login = new loginform();
    login.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

